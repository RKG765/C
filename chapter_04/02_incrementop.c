#include<stdio.h>

int main(){
    int a= 3;
    printf("The value of a++ is: %d\n",a++); // a++ Here it will increase but it will print the original value but ++a will print the increased value.
    printf("The value of a is: %d\n",a);
    // operators like: a+=10; it will add 10 to the orignal value.
    return 0;
}